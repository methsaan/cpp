/*
Working with floats
*/

#include <cstdio>
#include <cstdlib>
#include <iostream>
using namespace std; 

int main(int argc, char* argv[])
{
    float f1;
    float f2;
    f1=3.11;
    f2=0.31;
    cout << "Float f1 is " << f1 << endl;
    cout << "Float f2 is " << f2 << endl;
    cout << "f1/f2 is " << f1/f2 << endl;
    cout << "f2/f1 is " << f2/f1 << endl;
}
